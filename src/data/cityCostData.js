// 构建完整 CITY_MAP，包含城市及其下辖区（区会继承城市的 workDaysPerMonth/hoursPerDay）
const cityMapEntries = []
for (const region of CITY_REGIONS) {
  for (const city of region.cities) {
    cityMapEntries.push([city.id, city])
    if (city.districts) {
      for (const district of city.districts) {
        cityMapEntries.push([
          district.id,
          {
            ...district,
            workDaysPerMonth: city.workDaysPerMonth,
            hoursPerDay: city.hoursPerDay,
            // 显示名用于确认文案，如"上海·静安区"
            displayName: `${city.name}·${district.name}`,
            parentCityId: city.id,
          },
        ])
      }
    }
  }
}
export const CITY_MAP = Object.fromEntries(cityMapEntries)
